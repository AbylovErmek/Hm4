package com.geektech.hm4;

public class FirstFragmentModel {

    private String name;
    private String surName;

    public FirstFragmentModel(String name, String surName) {
        this.name = name;
        this.surName = surName;
    }

    public String getName() {
        return name;
    }

    public String getSurName() {
        return surName;
    }
}
